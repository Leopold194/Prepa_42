void ft_print_comb()
{
  int n = '0';

  while (n < '8')
    {
      int j = '1';

      while (j < '9')
	{
	  int g = '2';

	  while (g <= '9')
	    {
	      if (j != n && g != j && g != n && g > j && j > n)
		{
		  write(1, &n, 1);
		  write(1, &j, 1);
		  write(1, &g, 1);
		  write(1, ", ", 1);
		}
	      g++;
	    }  
	  j++;
	}
      n++;
    }
}
